class AppLocalizationsDelegate extends LocalizationsDelegate<AppLocalizations> {
  const AppLocalizationsDelegate();

  @override
  bool isSupported(Locale locale) {
    return AppLocalizations.supportedLocales.any(
      (supported) =>
          supported.languageCode == locale.languageCode &&
          (supported.scriptCode == null ||
              supported.scriptCode == locale.scriptCode),
    );
  }

  @override
  Future<AppLocalizations> load(Locale locale) {
    final resolvedLocale = _resolveLocale(locale);
    return SynchronousFuture(AppLocalizations(resolvedLocale));
  }

  @override
  bool shouldReload(covariant LocalizationsDelegate<AppLocalizations> old) =>
      false;

  AppLocale _resolveLocale(Locale locale) {
    for (final supported in AppLocale.values) {
      final target = supported.locale;
      final sameLanguage = target.languageCode == locale.languageCode;
      final sameCountry =
          target.countryCode == null ||
          locale.countryCode == null ||
          target.countryCode == locale.countryCode;
      final sameScript =
          target.scriptCode == null ||
          locale.scriptCode == null ||
          target.scriptCode == locale.scriptCode;

      if (sameLanguage && sameCountry && sameScript) {
        return supported;
      }
    }
    return AppLocale.uzLatin;
  }
}
